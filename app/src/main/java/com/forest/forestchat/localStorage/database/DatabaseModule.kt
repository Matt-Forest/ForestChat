package com.forest.forestchat.localStorage.database

import android.app.Application
import androidx.room.Room
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@InstallIn(SingletonComponent::class)
@Module
class DatabaseModule {

    @Singleton
    @Provides
    fun provideDatabase(app: Application): Database {
        return Room
            .databaseBuilder(app, Database::class.java, "forestchat_db")
            .build()
    }

    @Singleton
    @Provides
    fun provideMessageDao(database: Database) = database.messageDao()

    @Singleton
    @Provides
    fun provideContactDao(database: Database) = database.contactDao()

    @Singleton
    @Provides
    fun provideConversationDao(database: Database) = database.conversationDao()

}